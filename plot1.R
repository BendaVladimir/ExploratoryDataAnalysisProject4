######################################################################################################
## Importing libraries
######################################################################################################

library(dplyr)
library(ggplot2)
library(stringr)


######################################################################################################
## Aggregating data for plotting 
######################################################################################################

yearly_data <- NEI %>%
  group_by(year) %>%
  summarize(Value = sum(Emissions))

######################################################################################################
## Plotting data to png format
######################################################################################################

png(file="plot1.png",
    width=480, height=480)
with(yearly_data, plot(year, Value, type = "l", main = "Total PM2.5 emission in USA", xlab = "Year", ylab = "PM2.5 value"))
dev.off()
